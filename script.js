var curplay= null;

window.onload = function() {
  const defaultp = document.getElementById('swayam'); 
  if (defaultp) {
    color(defaultp);
  }
};

 function color(a) {
  const cards = document.querySelectorAll('.lcard');
  cards.forEach(card => { card.classList.remove('lactive'); });
  a.classList.add('lactive');
  const cardContainer = document.getElementById("conofplay");
  cardContainer.innerHTML = ""; // Clear existing cards

  var ply = a.id;

  switch (ply) {
      case 'swayam':
          curplay = swayam; // Assign the swayam array to curplay
          document.getElementById("bannername").innerText="My Songs";   
          break;
      case 'bollywood':
          curplay = bollywood; // Assign the bollywood array to curplay
          document.getElementById("bannername").innerText="Bollywood";   
          break;
      case 'hiphop':
          curplay = hiphop; // Assign the hiphop array to curplay
          document.getElementById("bannername").innerText="Hiphop";   
          break;
      case 'oldgen':
          curplay = oldgen; // Assign the oldgen array to curplay
          document.getElementById("bannername").innerText="Oldgen";   
          break;
      case 'podcast':
        document.getElementById("bannername").innerText="Podcasts";   
        document.getElementById("conofplay").innerHTML=
        `
        <iframe style="border-radius:12px" src="https://open.spotify.com/embed/episode/1x1YHdOpYAjwR48eoz1yLL?utm_source=generator" width="100%"
         height="200" frameBorder="0" allowfullscreen="" allow="autoplay; clipboard-write; encrypted-media; fullscreen; picture-in-picture" loading="lazy">
         </iframe> 
         <iframe style="border-radius:12px" src="https://open.spotify.com/embed/episode/7fvS851vPtqtQg8oMo6JrO?utm_source=generator" width="100%" 
         height="200" frameBorder="0" allowfullscreen="" allow="autoplay; clipboard-write; encrypted-media; fullscreen; picture-in-picture" loading="lazy">
         </iframe> 
          <iframe style="border-radius:12px" src="https://open.spotify.com/embed/episode/3VnXFIu0PfGSYXneWkQJ2H?utm_source=generator&theme=0" width="100%" 
        height="200" frameBorder="0" allowfullscreen="" allow="autoplay; clipboard-write; encrypted-media; fullscreen; picture-in-picture" loading="lazy">
        </iframe>
        `
          break;

      default:
          curplay = []; 
  }

  if (curplay) {
      curplay.forEach(song => {
          const card = document.createElement("div");
          card.className = "acard";
          card.setAttribute("onclick", "acolor(this)");

          card.innerHTML = `
              <div class="no">
                  <span class="contentplay">${song.number}.</span>
              </div>
              <img class="sg" src="${song.cover}" alt="${song.title}">
              <div class="name">
                  <span class="contentplay">${song.title}</span><br>
                  <span class="contentart">${song.artist}</span>
              </div>
              <div class="duration">
                  <span class="time">${song.duration}</span>
              </div>
          `;
          cardContainer.appendChild(card);
      });
  }
}

let currentAudio = null; 
let currentCard = null;  
let isPlaying = false;   
const playPauseButton = document.getElementById('playPauseBtn');
const playPauseIcon = document.getElementById('playPauseIcon');

function acolor(acar) {
  const cards = document.querySelectorAll('.acard');
  const noDiv = acar.querySelector('.no');
  const cardIndex = Array.from(cards).indexOf(acar);

  if (!curplay || !curplay[cardIndex]) return;

  const song = curplay[cardIndex]; 
  const songUrl = song.url;

  if (currentAudio && currentCard === acar) {
    currentAudio.pause();
    currentCard.classList.remove('lactive');
    noDiv.innerHTML = `<span class="contentplay">${song.number}.</span>`;
    playPauseIcon.className = 'ph-bold ph-play';
    currentAudio = null;
    currentCard = null;
    isPlaying = false;
    return;
  }

  if (currentAudio) {
    currentAudio.pause();
    currentAudio.currentTime = 0;
    currentCard.classList.remove('lactive');
    currentCard.querySelector('.no').innerHTML = `<span class="contentplay">${curplay[Array.from(cards).indexOf(currentCard)].number}.</span>`;
  }

  currentCard = acar;
  currentAudio = new Audio(songUrl);
  acar.classList.add('lactive');
  noDiv.innerHTML = '<img class="cimg invert" src="img/graphic.gif">'; 
  currentAudio.play();
  isPlaying = true;
  playPauseIcon.className = 'ph-bold ph-pause'; 

  currentAudio.addEventListener('ended', () => {
    acar.classList.remove('lactive');
    noDiv.innerHTML = `<span class="contentplay">${song.number}.</span>`;
    playPauseIcon.className = 'ph-bold ph-play'; 
    currentAudio = null;
    currentCard = null;
    isPlaying = false;
  });
}

playPauseButton.addEventListener('click', () => {
  if (currentAudio) {
    if (isPlaying) {
      currentAudio.pause();
      playPauseIcon.className = 'ph-bold ph-play'; 
      isPlaying = false;
    } else {
      currentAudio.play();
      playPauseIcon.className = 'ph-bold ph-pause'; 
      isPlaying = true;
    }
  }
});
